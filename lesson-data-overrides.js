  function deepClone(value) {
    return JSON.parse(JSON.stringify(value));
  }

  function stripPrefix(title) {
    return title
      .replace(/^§\d+\.\s*/, "")
      .replace(/^№\d+\s+[^\.\n]+\.\s*/, "");
  }

  function buildIntro(title, kind) {
    const topic = stripPrefix(title);

    if (kind === "lab") {
      return `Бұл зертханалық жұмыста ${topic.toLowerCase()} бойынша бақылау, салыстыру және қорытынды жасау дағдылары қалыптастырылады.`;
    }

    if (kind === "practice") {
      return `Бұл практикалық жұмыста ${topic.toLowerCase()} тақырыбы бойынша есептер мен қолданбалы тапсырмалар орындалады.`;
    }

    return `Бұл сабақта ${topic.toLowerCase()} қарастырылады, негізгі ұғымдар жүйеленіп, бекіту тапсырмалары орындалады.`;
  }

